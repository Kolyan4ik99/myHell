#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	y;

	if (n == 0)
		write(fd, "0", 1);
	if (n > 0)
	{
		y = n % 10 + '0';
		n = (n - n % 10) / 10;
		if (n != 0)
			ft_putnbr_fd(n, fd);
		write(fd, &y, 1);
	}
	if (n < 0 && n > -2147483648)
	{
		n = -1 * n;
		write(fd, "-", 1);
		ft_putnbr_fd(n, fd);
	}
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
}
